package entities;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;
import com.fasterxml.jackson.annotation.JsonIgnore;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="logs")
@DynamoDBTable(tableName = "logs")
public class Log {

    @Id
    @JsonIgnore
    @DynamoDBAttribute
    @DynamoDBHashKey
    @DynamoDBAutoGeneratedKey
    private String id;
    @DynamoDBAttribute
    private String time;
    @JsonIgnore
    private long userId;
    @DynamoDBAttribute
    private String key;
    @DynamoDBAttribute
    private String value;

    public Log(String id, String time, long userId, String key, String value) {
        this.id = id;
        this.time = time;
        this.userId = userId;
        this.key = key;
        this.value = value;
    }

    public Log() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public long getUserId() {
        return userId;
    }

    public void setUserId(long userId) {
        this.userId = userId;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Log log = (Log) o;

        if (userId != log.userId) return false;
        if (id != null ? !id.equals(log.id) : log.id != null) return false;
        if (time != null ? !time.equals(log.time) : log.time != null) return false;
        if (key != null ? !key.equals(log.key) : log.key != null) return false;
        return !(value != null ? !value.equals(log.value) : log.value != null);

    }

    @Override
    public int hashCode() {
        int result = id != null ? id.hashCode() : 0;
        result = 31 * result + (time != null ? time.hashCode() : 0);
        result = 31 * result + (int) (userId ^ (userId >>> 32));
        result = 31 * result + (key != null ? key.hashCode() : 0);
        result = 31 * result + (value != null ? value.hashCode() : 0);
        return result;
    }

    @Override
    public String toString() {
        return "Log:  id =" + id + "\ttime =" + time.toString() + "\tuserid =" + userId + "\tkey =" + key + "\tvalue =" + value.toString();
    }
}
